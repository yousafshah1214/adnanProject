                        		<p>The PNSSP will be implemented through collaboration between the national space agency of Pakistan, SUPARCO, and selected universities nationwide. As conceptualized, this project will be fully sponsored and supervised by SUPARCO. The mechanism to impart know-how about satellite design and development to students is through design seminars, workshops and continuous guidance from SUPARCO engineers and senior experts.
This program will focus to enhance the capacity of universities in space engineering and technology through collaboration in design and development of small satellites for research, technology evaluation, and component validation purposes. It will also provide the opportunity for students to work in inter-disciplinary, inter-university and competitive technical project teams. The PNSSP is being launched with an idea to uplift the overall level of universities/institutes in the domain space technology through series of student satellites. In a nutshell, PNSS program will benefit the education sector as well as space industry of Pakistan.
